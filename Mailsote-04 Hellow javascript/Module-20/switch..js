const color = 'green';

switch (color) {
    case 'red':
        console.log("this is red color");
        break;
    case 'blue':
        console.log('this is blue color');
        break;
    case 'green':
        console.log('this is green color');
        break;
    case 'black':
    case 'dark':
        console.log('this is black color');
        break;    
    default:
        console.log('this is unknow color');
}